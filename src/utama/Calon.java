package utama;

public class Calon {
    protected String nama;
    protected int nim;
    protected int nilai_tulis;
    protected int nilai_coding;
    protected int nilai_wawancara;

    public Calon(String nama,int nim,int nilai_tulis,int nilai_coding,int nilai_wawancara) {
        this.nama = nama;
        this.nim = nim;
        this.nilai_tulis = nilai_tulis;
        this.nilai_coding = nilai_coding;
        this.nilai_wawancara = nilai_wawancara;
    }
}
